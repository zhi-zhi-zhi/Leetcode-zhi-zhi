function carFleet(target: number, position: number[], speed: number[]): number {
  let res = 0
  const stack = []
  const helpArr = position.map<[number, number, number]>((p, i) => [p, speed[i], i]).sort((a, b) => a[0] - b[0])
  const n = helpArr.length

  for (let i = n - 1; i >= 0;) {
    res++
    const [p, s, index] = helpArr[i]
    const time = (target - p) / s

    let j = i - 1
    while (j >= 0) {
      const [jP, jS, ji] = helpArr[j]

      const jTime = (target - jP) / jS
      if (jTime <= time || Math.abs(jTime - time) < 0.00001)
        j--
      else
        break
    }

    i = j
  }

  return res
};

